</li>

</ul>
<h6>2.5 PRINCIPIOS PARA EL TRATAMIENTO</h6>
<ul>
    <li>Principio de legalidad: El tratamiento que se da a los datos personales está ajustado a los parámetros establecidos por Ley.</li>
    <li>Principio de finalidad: Los datos personales objeto de tratamiento por parte de SITI MÁS serán utilizados únicamente con los fines anteriormente descritos.</li>
    <li>Principio de transparencia: El responsable del Tratamiento garantiza los Derechos del titular en cualquier momento y sin restricciones de esta manera es de gran importancia informar claramente los datos que son recolectados y el tratamiento de los mismos.</li>
    <li>
        Principio de seguridad: La información sujeta a tratamiento por parte de Siti Más Telecomunicaciones cuenta con las medidas exigidas por la Ley según la calidad del dato y a fin de evitar su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.
    </li>
    <li>
        Principio de confidencialidad: Todas las personas que intervienen en el Tratamiento de datos personales se encuentran obligadas a garantizar la reserva de la información, inclusive después de finalizada su relación con alguna de las labores que comprende el Tratamiento, pudiendo sólo realizar suministro o comunicación de datos personales cuando ello corresponda al desarrollo de las actividades autorizadas por la Ley o por el titular de los datos.
    </li>
    <li>
        Principio de veracidad o calidad: La información sujeta a tratamiento deberá ser veraz, completa, exacta, actualizada, comprobable, y comprensible. Se prohíbe el tratamiento de datos parciales, incompletos, fraccionados o induzcan a error.
    </li>
    <li>
        Principio de acceso y circulación restringida: El tratamiento se sujeta a los límites que se derivan de la naturaleza de los datos personales, de las disposiciones de la ley y la constitución política. En este sentido, el tratamiento solo podrá hacerse por personas autorizadas. Los datos personales, salvo la información pública, no podrán estar disponibles en internet u otros medios de divulgación o comunicación masiva, salvo que el acceso sea técnicamente controlable para brindar un conocimiento restringido solo a los titulares o terceros autorizados conforme a la ley.
    </li>

</ul>
<h6>2.6 MEDIDAS DE SEGURIDAD</h6>
<p>
    Las medidas de seguridad adoptadas por Siti Más Telecomunicaciones sobre sus bases de datos se ajustan a las previstas en la ley, con el fin de evitar accesos no autorizados, utilización, modificación, supresión, pérdida y/o utilización indebida de los datos personales. Todas las bases de datos, propiedad de Siti Más Telecomunicaciones cuentan con las medidas de seguridad básicas; dichas medidas se incrementan según la calidad del dato, acorde a lo previsto en las políticas de seguridad y privacidad de la información. Los datos personales captados en zonas de video vigilancia son almacenados y utilizados como mecanismo de vigilancia, protección y seguridad de los objetos, la información y las personas que frecuentan las instalaciones de Siti Más Telecomunicaciones. Dicha información será protegida a través de medios tecnológicos que restringen su acceso a personal no autorizado a través del uso de usuarios y contraseñas; además, se almacenará en la base de datos de videovigilancia por un término máximo de 60 días; con posterioridad será automáticamente suprimida del disco duro y el servidor.
</p>
<h6>
    2.7 DERECHOS DEL TITULAR DE LOS DATOS
</h6>

<p>
    Usted como titular de los datos recolectados por Siti Más Telecomunicaciones conforme a lo establecido en la Ley 1581 de 2012 y el Decreto 1074 de 2015, puede hacer uso de los siguientes derechos
</p>
<ul>
    <li>
        Derecho de Acceso: Es el derecho que tiene toda persona para conocer si sus datos personales están siendo objeto de tratamiento, se encuentran incluidos en la base de datos, que finalidad y origen tienen estos, así como las cesiones realizadas o previstas a terceros. Para ejercer su derecho podrá presentar una solicitud ante el responsable del Tratamiento o por medio de los canales de contacto ya especificados. Es importante resaltar que el titular podrá consultar de forma gratuita sus datos personales al menos una vez cada mes calendario y cada vez que existan modificaciones sustanciales de las Políticas de Tratamiento de la información. Cuando la consulta se realice más de una vez cada mes Siti Más Telecomunicaciones como propietario de la base de datos, podrá cobrar los gastos de envío, reproducción y si se requiere certificación de documentos.
    </li>
    <li>
        Derecho de Actualización y Rectificación: Es el derecho que tiene usted como titular de los datos para que se actualicen y corrijan los datos personales que se encuentran en las bases de datos propiedad de Siti Más Telecomunicaciones cuando estos hayan cambiado, cuando exista un error, sean inexactos o incompletos. Para ello, igualmente, se debe presentar una solicitud de información que debe contener, adicionalmente a lo señalado en el punto anterior sobre el acceso a datos personales, las modificaciones a realizarse y aportar la documentación que sustente su petición.
    </li>
    <li>
        Derecho de Supresión: Es el derecho que usted como usuario tiene para que se supriman sus datos personales en las bases de datos propiedad de Siti Más Telecomunicaciones en los siguiente casos: (i) cuando el tratamiento de los mismos no se ajuste a lo dispuesto por la Ley de Protección de Datos (Ley 1581 de 2012) siempre y cuando la Superintendencia de Industria y Comercio haya determinado que en el Tratamiento el Responsable o Encargado han incurrido en conductas contrarias al ordenamiento y (ii) en virtud de la solicitud libre y voluntaria del Titular del dato, siempre y cuando no exista una obligación legal o contractual que imponga al Titular el deber de permanecer en la referida base de datos. La supresión da lugar al bloqueo de sus datos, esto quiere decir que los datos se conservan únicamente a disposición de autoridades competentes, con fines de seguridad y para atender las posibles responsabilidades nacidas del tratamiento.
    </li>
    <li>
        Presentación de quejas: Como titular de los datos tiene derecho de presentar ante la Superintendencia de Industria y Comercio quejas por infracciones asociadas al uso de sus datos personales, siempre y cuando haya agotado el trámite de consulta o reclamo ante Siti Más Telecomunicaciones.
    </li>
</ul>
<h6>
    2.8 DEBERES DEL RESPONSABLE Y ENCARGADOS DEL TRATAMIENTO
</h6>
<p>
    El responsable y los encargados del tratamiento deben cumplir con las siguientes disposiciones previstas en la Ley 1581 de 2012 y el Decreto 1074 de 2015.
</p>
<h5>
    Responsable del Tratamiento
</h5>
<p>
a) Garantizar al Titular, en todo tiempo, el pleno y efectivo ejercicio del derecho de hábeas data.
b) Solicitar y conservar, en las condiciones previstas en la presente ley, copia de la respectiva autorización otorgada por el Titular;
c) Informar debidamente al Titular sobre la finalidad de la recolección y los derechos que le asisten por virtud de la autorización otorgada.
d) Conservar la información bajo las condiciones de seguridad necesarias para impedir su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.
e) Garantizar que la información que se suministre al Encargado del Tratamiento sea veraz, completa, exacta, actualizada, comprobable y comprensible.
f) Actualizar la información, comunicando de forma oportuna al Encargado del Tratamiento, todas las novedades respecto de los datos que previamente le haya suministrado y adoptar las demás medidas necesarias para que la información suministrada a este se mantenga actualizada.
g) Rectificar la información cuando sea incorrecta y comunicar lo pertinente al Encargado del Tratamiento.
h) Suministrar al Encargado del Tratamiento, según el caso, únicamente datos cuyo tratamiento esté previamente autorizado de conformidad con lo previsto en la presente ley.
i) Exigir al Encargado del Tratamiento en todo momento, el respeto a las condiciones de seguridad y privacidad de la información del Titular.
j) Tramitar las consultas y reclamos formulados en los términos señalados en la presente ley.
k) Adoptar un manual interno de políticas y procedimientos para garantizar el adecuado cumplimiento de la presente ley y en especial para la atención de consultas y reclamos.
l) Informar al Encargado del Tratamiento cuando determinada información se encuentra en discusión por parte del Titular, una vez se haya presentado la reclamación y no haya finalizado el trámite respectivo.
m) Informar a solicitud del Titular sobre el uso dado a sus datos.
n) Informar a la autoridad de protección de datos cuando se presenten violaciones a los códigos de seguridad y existan riesgos en la administración de la información de los Titulares.
o) Cumplir las instrucciones y requerimientos que imparta la Superintendencia de Industria y Comercio.
</p>
<h5>
    Encargados del tratamiento
</h5>

<p>
    a) Garantizar al Titular, en todo tiempo, el pleno y efectivo ejercicio del derecho de hábeas data.
b) Conservar la información bajo las condiciones de seguridad necesarias para impedir su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.
c) Realizar oportunamente la actualización, rectificación o supresión de los datos en los términos de la presente ley.
d) Actualizar la información reportada por los responsables del Tratamiento dentro de los cinco (5) días hábiles contados a partir de su recibo;
e) Tramitar las consultas y los reclamos formulados por los Titulares en los términos señalados en la presente ley.
f) Adoptar un manual interno de políticas y procedimientos para garantizar el adecuado cumplimiento de la ley y, en especial, para la atención de consultas y reclamos por parte de los Titulares.
g) Registrar en la base de datos la leyenda "reclamo en trámite" en la forma en que se regula en la presente ley.
h) Insertar en la base de datos la leyenda "información en discusión judicial" una vez notificado por parte de la autoridad competente sobre procesos judiciales relacionados con la calidad del dato personal.
i) Abstenerse de circular información que esté siendo controvertida por el Titular y cuyo bloqueo haya sido ordenado por la Superintendencia de Industria y Comercio.
j) Permitir el acceso a la información únicamente a las personas que pueden tener acceso a ella.
k) Informar a la Superintendencia de Industria y Comercio cuando se presenten violaciones a los códigos de seguridad y existan riesgos en la administración de la información de los Titulares.
l) Cumplir las instrucciones y requerimientos que imparta la Superintendencia de Industria y Comercio.

Parágrafo. En el evento en que concurran las calidades de Responsable del Tratamiento y Encargado del Tratamiento en la misma persona, le será exigible el cumplimiento de los deberes previstos para cada uno.

</p>

