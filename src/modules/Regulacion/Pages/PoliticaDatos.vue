<h2>
POLÍTICAS DE TRATAMIENTO DE LA INFORMACIÓN LEY 1581 DE 2012 – DECRETO 1377 DE 2013
</h2>

<h3>
Política Tratamiento y protección de datos personales.
</h3>

<h5>1. OBJETIVO Y ALCANCE</h5>

<p>
Establecer los criterios sobre el uso, tratamiento, procesamiento, intercambio, transferencia y transmisión de datos personales, Esta política aplica para todos los titulares de información personal que sea utilizada o se encuentre en las bases de datos de Siti Más Telecomunicaciones en lo sucesivo "Siti Más Telecomunicaciones", quien actúa en calidad de responsable del tratamiento de los datos personales.
</p>

<h5>2. POLÍTICAS</h5>

<h6>2.1 POLÍTICA DE TRATAMIENTO DE SITI MÁS TELECOMUNICACIONES</h6>

<p>
De conformidad con lo dispuesto en la Ley 1581 de 2012, el Decreto 1074 de 2015, el Decreto1078 de 2015 y las Circulares Externas No. 02 del 3 de Noviembre de 2015 y 001 del 8 de Noviembre de 2016, se actualiza la Política de Tratamiento de Datos Personales.
</p>

<h6>2.2 RESPONSABLE DE LOS DATOS PERSONALES</h6>

<p>
La persona jurídica responsable de los datos personales y por tanto de las bases de datos en la cual se encuentren ubicados los mismos Siti Más Telecomunicaciones, con NIT 901.810.360-0, ubicada en la Corregimiento de Cuatro Esquinas del municipio de El Tambo-Cauca.
</p>

<h6>2.3 INFORMACIÓN RECOPILADA</h6>

<p>
Datos personales públicos, semiprivados, privados y sensibles de usuarios y clientes potenciales, trabajadores y extrabajadores, pensionados, contratistas, proveedores, miembros de junta directiva y visitantes tales como son: nombre, apellidos, identificación, teléfono fijo y/o móvil, dirección física y electrónica, imágenes e incluso aquella considerada como de carácter sensible, vale decir, huella dactilar (física o digital).
</p>

<p>
Tratándose de trabajadores, se recauda información relativa a datos biométricos, fotografías, imágenes; información de índole socioeconómica, patrimonial, tributaria, relativa a temas sindicales, composición familiar, referencias comerciales; la relacionada con su salud y demás que se le requiera para el buen desarrollo del vínculo contractual y demás datos necesarios que le sean solicitados al momento de su vinculación.
</p>

<p>
Se recolecta la huella digital de sus usuarios con la autorización expresa del titular, exclusivamente, para efectos de evitar suplantaciones de identidad y, en general, como herramienta de prevención y control de fraudes.
</p>

<p>
Las imágenes de las personas captadas por las cámaras de seguridad ubicadas en zonas de video vigilancia son almacenadas y utilizadas como mecanismo de protección y seguridad de los objetos, la información y las personas que frecuentan las instalaciones de la empresa.
</p>

<h6>2.4 TRATAMIENTO Y FINALIDAD</h6>

<p>
Los datos recabados por Siti Más Telecomunicaciones serán tratados con el grado de protección adecuado, exigido por la Ley y en este sentido el responsable del tratamiento se compromete a tratar los datos con la finalidad exclusiva para la que fueron recolectados.
</p>

<p>
Siti Más Telecomunicaciones recopila datos de sus usuarios y clientes potenciales con los siguientes fines.
</p>

<ul>

<li>
Identificarlo como usuario de nuestros servicios y adelantar las gestiones necesarias para su mejor prestación.
</li>

<li>
Ofrecerle información relacionada con nuestros productos, servicios, ofertas, promociones, alianzas, concursos, contenidos, así como los de nuestras compañías vinculadas o asociadas.
</li>

<li>
Envío de información correspondiente al estado de su cuenta y obligaciones.
</li>

<li>
Contactarlo por cualquier medio que haya registrado para el agendamiento de visitas propias de la prestación del servicio.
</li>

<li>
