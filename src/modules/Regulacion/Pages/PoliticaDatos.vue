   2.9 OFICIAL DE PROTECCION DE DATOS 
</h6>
<p>
    Siti Más Telecomunicaciones ha designado una persona encargada y/o responsable de ejercer el rol de oficial de protección de datos, quien debe realizar sus funciones y ejecutar todas las actividades con apego a la legislación que regula la materia y en los términos que establece la guía para la implementación del principio de responsabilidad demostrada (Accountability), publicada por la Superintendencia de Industria y Comercio.
</p>
<h6>
    2.10 FUNCIONES DEL OFICIAL DE PROTECCION DE DATOS
</h6>
<p>
    
La función principal del oficial de protección de datos será velar por la implementación efectiva de las políticas y procedimiento adoptados por la Empresa para cumplir las normas, así como la implementación de buenas prácticas de gestión de datos personales dentro de la Empresa. Adicionalmente ejercerá las siguientes funciones: • Velar en coordinación con la oficina de PQR por el trámite efectivo y oportuno a las solicitudes elevadas por los Titulares de los datos.
</p>
<ul>
    <li>
        Estructurar, diseñar y administrar el Programa que permitirá cumplir con las normas que regulan el tema de protección de datos personales.
    </li>
    <li>
        Establecer las responsabilidades específicas de las áreas de la empresa frente a la recolección, almacenamiento, uso, actualización, circulación y eliminación de los datos.
    </li>
    <li>
        Establecer los controles al Programa y su evaluación y revisión permanente.
    </li>
</ul>
